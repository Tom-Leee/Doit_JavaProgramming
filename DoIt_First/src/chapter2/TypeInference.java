package chapter2;

public class TypeInference {

	public static void main(String[] args) {
		//int j = 486으로 컴파일
		var j = 486;
		
		//double t = 3.14로 컴파일
		var t = 3.14;
		
		//String l = "주뗌리"로 컴파일
		var l = "주뗌리";
		
		System.out.println(j);
		System.out.println(t);
		System.out.println(l);
	}

}

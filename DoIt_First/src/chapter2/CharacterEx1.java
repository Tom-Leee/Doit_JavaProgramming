package chapter2;

public class CharacterEx1 {

	public static void main(String[] args) {
		char ascii1 = 'J';
		//문자 출력
		System.out.println(ascii1);
		//문자에 해당하는 정수 값(아스키 코드 값) 출력
		System.out.println((int)ascii1);	
		
		//정수 값 대입
		char ascii2 = 84;
		//정수 값에 해당하는 문자 출력
		System.out.println(ascii2);

		int ascii3 = 76;
		//문자 정수 값 출력
		System.out.println(ascii3);
		//정수 값에 해당하는 문자 출력
		System.out.println((char)ascii3);

	}

}
